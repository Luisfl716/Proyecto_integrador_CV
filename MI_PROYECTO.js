//Progreso de la barra de habilidades
function efectoHabilidades(){
    var skills = document.getElementById("skills");
    var distancia_skills = window.innerHeight - skills.getBoundingClientRect().top;
    if(distancia_skills >= 300){
        let habilidades = document.getElementsByClassName("progreso");
        habilidades[0].classList.add("mecanica");
        habilidades[1].classList.add("mantenimiento");
        habilidades[2].classList.add("electricidad");
        habilidades[3].classList.add("dise√±o");
        habilidades[4].classList.add("comunicacion");
        habilidades[5].classList.add("trabajo");
        habilidades[6].classList.add("creatividad");
        habilidades[7].classList.add("dedicacion");
    }
}

//Aplica la animacion de la barra de habilidades
window.onscroll = function(){
    efectoHabilidades();
}
